function 색상변경(color) {
    switch(color) {
        case "lightblue":
            document.getElementById("bd").style.backgroundColor = "lightblue";
            break;
        case "lightcoral" :
            document.getElementById("bd").style.backgroundColor = "lightcoral";
            break;
        case "lightyellow" :
            document.getElementById("bd").style.backgroundColor = "lightyellow";
            break;
        case "lightgreen" :
            document.getElementById("bd").style.backgroundColor = "lightgreen";
            break;
        case "lightgrey" :
            document.getElementById("bd").style.backgroundColor = "lightgrey";
            break;
    }
}